#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

/*	[F]
	[ Definition ]
	Print a string character by character using the [f]ft_putchar.
*/
void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
