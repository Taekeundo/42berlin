#include <unistd.h>

/*	[F]
	[ Definition ]
	print out '0' to '9' one char by one char.
*/
void	ft_print_numbers(void)
{
	char	c;

	c = '0';
	while (c <= '9')
	{
		write(1, &c, 1);
		c++;
	}
}
