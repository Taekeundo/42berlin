// Color definitions using ANSI escape sequences
#define RED "\e[1;31m"    // Red
#define WHI "\e[0;37m"    // White
#define GRE "\e[1;32m"    // Green
#define YEL "\e[1;33m"    // Yellow
#define BLU "\e[1;34m"    // Blue
#define MAG "\e[1;35m"    // Magenta
#define CYN "\e[1;36m"    // Cyan
#define BLK "\e[0;30m"    // Black
#define BOLD "\e[1m"      // Bold text
#define RESET "\e[0m"     // Reset color to default

// Additional color combinations
#define LRED "\e[0;31m"   // Light Red
#define LWHI "\e[0;37m"   // Light White
#define LGRE "\e[0;32m"   // Light Green
#define LYEL "\e[0;33m"   // Light Yellow
#define LBLU "\e[0;34m"   // Light Blue
#define LMAG "\e[0;35m"   // Light Magenta
#define LCYN "\e[0;36m"   // Light Cyan
#define LBLK "\e[1;30m"   // Light Black

// Background Colors
#define BRED "\e[41m"    // Red Background
#define BGRN "\e[42m"    // Green Background
#define BYEL "\e[43m"    // Yellow Background
#define BBLU "\e[44m"    // Blue Background
#define BMAG "\e[45m"    // Magenta Background
#define BCYN "\e[46m"    // Cyan Background
#define BWHI "\e[47m"    // White Background
#define BBLK "\e[40m"    // Black Background